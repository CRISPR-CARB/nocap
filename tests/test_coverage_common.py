class TestAssignWork:
    """Concrete examples for assign_work."""

    def test_single_task_gets_all_queries(self):
        """With n_tasks=1, task 0 gets every query index."""
        result = assign_work(5, 0, 1)
        assert result == [0, 1, 2, 3, 4]

    def test_two_tasks_even_split(self):
        """With n_tasks=2 and 4 queries, each task gets 2 indices."""
        assert assign_work(4, 0, 2) == [0, 2]
        assert assign_work(4, 1, 2) == [1, 3]

    def test_two_tasks_odd_queries(self):
        """With n_tasks=2 and 5 queries, task 0 gets 3, task 1 gets 2."""
        assert assign_work(5, 0, 2) == [0, 2, 4]
        assert assign_work(5, 1, 2) == [1, 3]

    def test_three_tasks_round_robin(self):
        """Round-robin partition across 3 tasks."""
        assert assign_work(9, 0, 3) == [0, 3, 6]
        assert assign_work(9, 1, 3) == [1, 4, 7]
        assert assign_work(9, 2, 3) == [2, 5, 8]

    def test_zero_queries_returns_empty(self):
        """Zero queries → every task gets an empty list."""
        assert assign_work(0, 0, 1) == []
        assert assign_work(0, 0, 3) == []

    def test_task_id_beyond_n_queries_returns_empty(self):
        """task_id >= n_queries → empty list (no work to do)."""
        assert assign_work(2, 2, 5) == []
        assert assign_work(2, 4, 5) == []

    def test_partition_covers_all_indices(self):
        """Union of all tasks' indices == range(n_queries)."""
        n_queries, n_tasks = 13, 4
        all_indices = []
        for t in range(n_tasks):
            all_indices.extend(assign_work(n_queries, t, n_tasks))
        assert sorted(all_indices) == list(range(n_queries))

    def test_partition_is_disjoint(self):
        """No index appears in more than one task's list."""
        n_queries, n_tasks = 11, 3
        seen = set()
        for t in range(n_tasks):
            indices = assign_work(n_queries, t, n_tasks)
            for i in indices:
                assert i not in seen, f"Index {i} assigned to multiple tasks"
                seen.add(i)

    def test_n_tasks_equals_n_queries(self):
        """When n_tasks == n_queries, each task gets exactly one index."""
        n = 5
        for t in range(n):
            assert assign_work(n, t, n) == [t]

    def test_large_n_tasks_relative_to_queries(self):
        """When n_tasks > n_queries, some tasks get empty lists."""
        result_0 = assign_work(3, 0, 10)
        result_5 = assign_work(3, 5, 10)
        assert result_0 == [0]
        assert result_5 == []


