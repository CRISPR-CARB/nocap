class TestRowsToMatrix:
    """Concrete examples for rows_to_matrix."""

    def test_empty_rows(self):
        """Empty rows → empty candidate_set and empty lookup."""
        candidate_set, labels_out, lookup = rows_to_matrix([], ["tf1->out1"])
        assert candidate_set == []
        assert lookup == {}

    def test_single_row(self):
        """Single row produces one candidate and one lookup entry."""
        rows = [["tf1", "cand1", "out1", True]]
        candidate_set, labels_out, lookup = rows_to_matrix(rows, ["tf1->out1"])
        assert candidate_set == ["cand1"]
        assert lookup[("cand1", "tf1->out1")] is True

    def test_candidate_set_is_sorted(self):
        """candidate_set is always sorted alphabetically."""
        rows = [
            ["tf1", "zebra", "out1", True],
            ["tf1", "alpha", "out1", False],
            ["tf1", "mango", "out1", True],
        ]
        candidate_set, _, _ = rows_to_matrix(rows, ["tf1->out1"])
        assert candidate_set == sorted(candidate_set)
        assert candidate_set == ["alpha", "mango", "zebra"]

    def test_lookup_key_format(self):
        """Lookup keys are (candidate, 'tf1->outcome') strings."""
        rows = [["tf1", "cand1", "out1", False]]
        _, _, lookup = rows_to_matrix(rows, ["tf1->out1"])
        assert ("cand1", "tf1->out1") in lookup
        assert lookup[("cand1", "tf1->out1")] is False

    def test_multiple_queries_multiple_candidates(self):
        """Matrix with 2 queries × 2 candidates has 4 lookup entries."""
        rows = [
            ["tf1", "cand1", "out1", True],
            ["tf1", "cand2", "out1", False],
            ["tf2", "cand1", "out2", False],
            ["tf2", "cand2", "out2", True],
        ]
        labels = ["tf1->out1", "tf2->out2"]
        candidate_set, labels_out, lookup = rows_to_matrix(rows, labels)
        assert set(candidate_set) == {"cand1", "cand2"}
        assert lookup[("cand1", "tf1->out1")] is True
        assert lookup[("cand2", "tf1->out1")] is False
        assert lookup[("cand1", "tf2->out2")] is False
        assert lookup[("cand2", "tf2->out2")] is True

    def test_query_labels_passed_through(self):
        """The second element of the return tuple is the input query_labels."""
        labels = ["tf1->out1", "tf2->out2"]
        rows = [["tf1", "cand1", "out1", True]]
        _, labels_out, _ = rows_to_matrix(rows, labels)
        assert labels_out is labels  # same object

    def test_found_values_are_bool(self):
        """Lookup values are booleans."""
        rows = [["tf1", "cand1", "out1", 1]]  # int 1 → bool True
        _, _, lookup = rows_to_matrix(rows, ["tf1->out1"])
        val = lookup[("cand1", "tf1->out1")]
        assert isinstance(val, bool)
        assert val is True

    def test_return_is_three_tuple(self):
        """Return value is a 3-tuple."""
        result = rows_to_matrix([], [])
        assert isinstance(result, tuple)
        assert len(result) == 3
