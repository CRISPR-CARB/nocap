class TestMergeShards:
    """Concrete examples for merge_shards."""

    def test_empty_shard_list(self):
        """Empty input → empty output."""
        assert merge_shards([]) == []

    def test_single_shard_passthrough(self):
        """Single shard with no duplicates is returned as-is."""
        shard = [
            ["tf1", "cand1", "out1", True],
            ["tf1", "cand2", "out1", False],
        ]
        result = merge_shards([shard])
        assert len(result) == 2
        assert result[0] == ["tf1", "cand1", "out1", True]
        assert result[1] == ["tf1", "cand2", "out1", False]

    def test_two_disjoint_shards_merged(self):
        """Two shards with no overlapping keys are fully merged."""
        shard_a = [["tf1", "cand1", "out1", True]]
        shard_b = [["tf1", "cand2", "out1", False]]
        result = merge_shards([shard_a, shard_b])
        assert len(result) == 2

    def test_duplicate_key_first_wins(self):
        """When two shards share a key, the first occurrence wins."""
        shard_a = [["tf1", "cand1", "out1", True]]
        shard_b = [["tf1", "cand1", "out1", False]]  # same key, different value
        result = merge_shards([shard_a, shard_b])
        assert len(result) == 1
        assert result[0][3] is True  # first shard's value kept

    def test_all_duplicates_collapses_to_one(self):
        """Three shards all containing the same row → one row in output."""
        row = ["tf1", "cand1", "out1", True]
        result = merge_shards([[row], [row], [row]])
        assert len(result) == 1

    def test_output_rows_are_lists(self):
        """Output rows are always lists (even if input rows were tuples)."""
        shard = [("tf1", "cand1", "out1", True)]
        result = merge_shards([shard])
        assert isinstance(result[0], list)

    def test_no_duplicate_keys_in_output(self):
        """Output never has duplicate (tf1, candidate, outcome) keys."""
        shards = [
            [["A", "B", "C", True], ["A", "D", "C", False]],
            [["A", "B", "C", False], ["A", "E", "C", True]],  # A,B,C is dup
        ]
        result = merge_shards(shards)
        keys = [(r[0], r[1], r[2]) for r in result]
        assert len(keys) == len(set(keys))

    def test_all_rows_have_four_elements(self):
        """Every row in the output has exactly 4 elements."""
        shards = [
            [["tf1", "c1", "o1", True], ["tf1", "c2", "o1", False]],
            [["tf2", "c1", "o2", True]],
        ]
        result = merge_shards(shards)
        assert all(len(r) == 4 for r in result)

    def test_empty_shards_within_list(self):
        """Empty shards within the list are silently skipped."""
        result = merge_shards([[], [["tf1", "c1", "o1", True]], []])
        assert len(result) == 1


